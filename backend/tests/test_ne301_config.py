class TestVersionParsingIntegration:
    """测试版本号解析集成功能"""

    def test_parse_real_version_mk(self, tmp_path: Path):
        """集成测试：解析真实的 version.mk 文件"""
        # 模拟真实的 version.mk 文件
        version_mk = tmp_path / "version.mk"
        version_mk.write_text(
            "# NE301 SDK Version Configuration\n"
            "# Auto-generated by build system\n"
            "\n"
            "VERSION_MAJOR := 3\n"
            "VERSION_MINOR := 0\n"
            "VERSION_PATCH := 0\n"
            "VERSION_BUILD := 1\n"
        )

        toolchain = get_ne301_toolchain(tmp_path)
        version = toolchain.get_model_version()

        # 验证完整解析流程
        assert version.major == 3
        assert version.minor == 0
        assert version.patch == 0
        assert version.build == 1
        assert str(version) == "3.0.0.1"

    def test_toolchain_caching(self, tmp_path: Path):
        """测试工具链缓存机制"""
        # 第一次获取
        toolchain1 = get_ne301_toolchain(tmp_path)

        # 第二次获取（应该返回缓存的实例）
        toolchain2 = get_ne301_toolchain(tmp_path)

        # 验证是同一个实例
        assert toolchain1 is toolchain2

    def test_different_project_paths(self, tmp_path: Path):
        """测试不同项目路径的工具链实例"""
        # 创建两个不同的项目路径
        project1 = tmp_path / "project1"
        project2 = tmp_path / "project2"
        project1.mkdir()
        project2.mkdir()

        # 为项目 1 创建 version.mk
        version_mk1 = project1 / "version.mk"
        version_mk1.write_text(
            "VERSION_MAJOR := 2\n"
            "VERSION_MINOR := 0\n"
            "VERSION_PATCH := 0\n"
            "VERSION_BUILD := 100\n"
        )

        # 为项目 2 创建 version.mk
        version_mk2 = project2 / "version.mk"
        version_mk2.write_text(
            "VERSION_MAJOR := 3\n"
            "VERSION_MINOR := 1\n"
            "VERSION_PATCH := 0\n"
            "VERSION_BUILD := 200\n"
        )

        # 获取两个项目的工具链
        toolchain1 = get_ne301_toolchain(project1)
        toolchain2 = get_ne301_toolchain(project2)

        # 验证是不同的实例
        assert toolchain1 is not toolchain2

        # 验证版本号不同
        version1 = toolchain1.get_model_version()
        version2 = toolchain2.get_model_version()

        assert version1.to_tuple() == (2, 0, 0, 100)
        assert version2.to_tuple() == (3, 1, 0, 200)

