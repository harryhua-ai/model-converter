class TestExtractVersionVar:
    """测试 _extract_version_var() 版本变量解析"""

    def test_extract_with_colon_assign(self, tmp_path: Path):
        """测试使用 := 语法的变量提取（标准语法）"""
        toolchain = get_ne301_toolchain(tmp_path)
        content = "VERSION_MAJOR := 2\nVERSION_MINOR := 0\n"

        major = toolchain._extract_version_var(content, "VERSION_MAJOR", 3)
        minor = toolchain._extract_version_var(content, "VERSION_MINOR", 1)

        assert major == 2
        assert minor == 0

    def test_extract_missing_var(self, tmp_path: Path):
        """测试缺失变量的情况（应返回默认值）"""
        toolchain = get_ne301_toolchain(tmp_path)
        content = "VERSION_MAJOR := 2\n"

        major = toolchain._extract_version_var(content, "VERSION_MAJOR", 3)
        minor = toolchain._extract_version_var(content, "VERSION_MINOR", 0)
        patch = toolchain._extract_version_var(content, "VERSION_PATCH", 1)

        # 存在的变量返回实际值
        assert major == 2
        # 缺失的变量返回默认值
        assert minor == 0
        assert patch == 1

    def test_extract_with_comments(self, tmp_path: Path):
        """测试包含注释的变量提取"""
        toolchain = get_ne301_toolchain(tmp_path)
        content = (
            "# Version configuration\n"
            "VERSION_MAJOR := 2  # Major version\n"
            "VERSION_MINOR := 0  # Minor version\n"
        )

        major = toolchain._extract_version_var(content, "VERSION_MAJOR", 3)
        minor = toolchain._extract_version_var(content, "VERSION_MINOR", 1)

        # 应该正确处理注释
        assert major == 2
        assert minor == 0


